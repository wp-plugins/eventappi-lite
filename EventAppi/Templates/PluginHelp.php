    <h3>Using the EventAppi plugin</h3>
    <hr>
    <p>
        To start using EventAppi simply create a New Post, and add
        <code>[eventappi]</code> to the post content.</p>

    <p>
        EventAppi pages and shortcodes:<br><em>If accidentally deleted, these can be
            re-created by deactivating and re-activating the plugin.</em>
    </p>

    <p>
        <code>[eventappi_cart]</code> The Cart displays all the
        Items (tickets) the user wants to buy.
    </p>

    <p>
        <code>[eventappi_checkout]</code> Checkout Form - where the
        user can enter payment details and complete the ticket purchase.
    </p>

    <p>
        <code>[eventappi_login]</code> Login Form.
    </p>


    <p>
        <code>[eventappi_analytics]</code> Detailed statistics for
        Events/Tickets sales.
    </p>

    <p>
        <code>[eventappi_my_account]</code> My Account page that
        lists user profile and settings.
    </p>

